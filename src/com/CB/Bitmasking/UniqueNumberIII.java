package com.CB.Bitmasking;

import java.util.Scanner;

public class UniqueNumberIII {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int n = s.nextInt();
        int[] nums = new int[n];
        for (int i = 0; i < n; i++) {
            nums[i] = s.nextInt();
        }
        System.out.println();
    }
}
